def count_letter(words, letter):
    # Переменная для хранения количества слов, содержащих букву
  count = 0
    # Проходим по каждому слову в списке
  for word in words:
      # Если буква есть в слове, увеличиваем счётчик
    if letter in word:
        count += 1
    # Возвращаем итоговое количество
  return count


# Пример использования
words_list = ['python', 'c++', 'c', 'scala', 'java']
letter_to_find = 'c'
result = count_letter(words_list, letter_to_find)
print(result)  # Ожидаемый результат: 3